function tambah(bil1,bil2) {
    return bil1+bil2
}
function pengurangan(bil1,bil2) {
    return bil1-bil2
}
function perkalian(bil1,bil2) {
    return bil1*bil2
}
function pembagian(bil1,bil2){
    return bil1/bil2
}

let hasil=tambah(5,7)
document.write("hasil="+hasil);

hasil=pengurangan(10,1);
document.write("hasil="+hasil);

hasil=perkalian(3,3)
document.write("hasil"+hasil);

hasil=pembagian(10,2)
document.write("hasil"+hasil+"</br>");
document.write("hasil"+hasil);